namespace calculadora
{
    public class Funcao
    {
        public float Adicao(float n1, float n2){
            return n1 * n2;
        }
        public float Subtracao(float n1, float n2){
            return n1 - n2;
        }
        public float Multiplicacao(float n1, float n2){
            return n1 * n2;
        }
        public float Divisao(float n1, float n2){
            return n1 / n2;
        }
    }
}